# EnvNodeFactory — Environmental node generator
# ============================================================================

class EnvNodeFactory:
    """
    Generates EnvironmentalNode instances at fixed or semi-random positions.

    Environmental nodes represent global constraints:
      - south_facing: placed at the northern edge (south-facing exposure)
      - main_road_access: placed at the site entry side
      - playground: outdoor activity area
      - green_space: landscape/view amenity
    """

    def __init__(
        self,
        site_bounds: Tuple[float, float, float, float],
        rng: Optional[np.random.Generator] = None,
    ):
        """
        Args:
            site_bounds: (min_x, min_y, max_x, max_y) in meters.
            rng: NumPy random generator for reproducibility.
        """
        self.min_x, self.min_y, self.max_x, self.max_y = site_bounds
        self._rng = rng if rng is not None else np.random.default_rng()
        self._counters: Dict[EnvNodeType, int] = {}

    def _next_id(self, env_type: EnvNodeType) -> str:
        count = self._counters.get(env_type, 0)
        self._counters[env_type] = count + 1
        return f"{env_type.value}_{count:02d}"

    def generate_south_facing(self) -> EnvironmentalNode:
        """
        Generate the south-facing environmental node.

        Position: center of the southern edge (max_y, assuming Y+ = south).
        This node acts as the primary daylight source.
        """
        x = (self.min_x + self.max_x) / 2.0
        y = self.max_y  # Southern boundary
        return EnvironmentalNode(
            env_id=self._next_id(EnvNodeType.SOUTH_FACING),
            env_type=EnvNodeType.SOUTH_FACING,
            position=(x, y),
            attributes={'solar_orientation': 1.0},  # perfect south
        )

    def generate_main_road_access(self, road_side: str = "west") -> EnvironmentalNode:
        """
        Generate the main road access node.

        Position: center of the specified road side.
        Road side determines which boundary the access is on.

        Args:
            road_side: 'west', 'east', 'south', or 'north'.
        """
        if road_side == "west":
            pos = (self.min_x, (self.min_y + self.max_y) / 2.0)
        elif road_side == "east":
            pos = (self.max_x, (self.min_y + self.max_y) / 2.0)
        elif road_side == "south":
            pos = ((self.min_x + self.max_x) / 2.0, self.max_y)
        elif road_side == "north":
            pos = ((self.min_x + self.max_x) / 2.0, self.min_y)
        else:
            raise ValueError(f"Invalid road_side: {road_side}")

        return EnvironmentalNode(
            env_id=self._next_id(EnvNodeType.MAIN_ROAD_ACCESS),
            env_type=EnvNodeType.MAIN_ROAD_ACCESS,
            position=pos,
            attributes={'access_type': 1.0},  # 1 = main
        )

    def generate_playground(self) -> EnvironmentalNode:
        """
        Generate a playground environmental node.

        Position: semi-random location within the site, biased toward
        an open area on the east or west side.
        """
        x = self._rng.uniform(self.min_x + 20, self.max_x - 20)
        y = self._rng.uniform(self.min_y + 10, self.max_y * 0.4)
        return EnvironmentalNode(
            env_id=self._next_id(EnvNodeType.PLAYGROUND),
            env_type=EnvNodeType.PLAYGROUND,
            position=(x, y),
            attributes={'area_approx': 2000.0, 'is_outdoor': 1.0},
        )

    def generate_green_space(self) -> EnvironmentalNode:
        """
        Generate a green space environmental node.

        Position: random location near site boundary, for view quality.
        """
        # Place near a random boundary
        side = self._rng.integers(0, 4)
        if side == 0:  # north
            x = self._rng.uniform(self.min_x, self.max_x)
            y = self.min_y + 5.0
        elif side == 1:  # south
            x = self._rng.uniform(self.min_x, self.max_x)
            y = self.max_y - 5.0
        elif side == 2:  # west
            x = self.min_x + 5.0
            y = self._rng.uniform(self.min_y, self.max_y)
        else:  # east
            x = self.max_x - 5.0
            y = self._rng.uniform(self.min_y, self.max_y)

        return EnvironmentalNode(
            env_id=self._next_id(EnvNodeType.GREEN_SPACE),
            env_type=EnvNodeType.GREEN_SPACE,
            position=(x, y),
            attributes={'view_quality': self._rng.uniform(0.5, 1.0)},
        )

    def generate_all(self, school_size: str = "medium") -> List[EnvironmentalNode]:
        """
        Generate the standard set of environmental nodes.

        Args:
            school_size: 'small', 'medium', or 'large'. Determines
                         how many green_space nodes to generate.

        Returns:
            List of EnvironmentalNode instances.
        """
        nodes = [
            self.generate_south_facing(),
            self.generate_main_road_access(),
        ]

        # Playground for medium+ schools
        if school_size in ("medium", "large"):
            nodes.append(self.generate_playground())

        # Green spaces
        num_green = {"small": 1, "medium": 2, "large": 3}.get(school_size, 2)
        for _ in range(num_green):
            nodes.append(self.generate_green_space())

        return nodes
